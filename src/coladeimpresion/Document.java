package coladeimpresion;

/**
 * Clase Document, usada para representar los documentos a√±adidos por el usuario
 * @author manza
 */
public class Document {
    private String name;
    private String type;
    private String size;

    public Document(String name, String type, String size) {
        this.name = name;
        this.type = type;
        this.size = size;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

}
